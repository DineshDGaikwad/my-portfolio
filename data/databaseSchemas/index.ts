// ─── Database Schema Types ────────────────────────────────────────────────────

export type ColumnType =
  | 'INT' | 'BIGINT' | 'VARCHAR' | 'TEXT' | 'BOOLEAN'
  | 'DATETIME' | 'DECIMAL' | 'UUID' | 'ENUM' | 'JSON'

export interface Column {
  name: string
  type: ColumnType
  length?: number
  isPK?: boolean
  isFK?: boolean
  isNullable?: boolean
  isUnique?: boolean
  isIndex?: boolean
  defaultValue?: string
  references?: { table: string; column: string }
  enumValues?: string[]
}

export interface DBTable {
  id: string
  name: string
  color: string
  // Position as percentage of viewBox (0–100)
  x: number
  y: number
  columns: Column[]
  description?: string
}

export type RelationType = 'one-to-many' | 'many-to-many' | 'one-to-one'

export interface Relationship {
  id: string
  fromTable: string
  fromColumn: string
  toTable: string
  toColumn: string
  type: RelationType
  label?: string
}

export interface DatabaseSchema {
  projectId: string
  projectSlug: string
  projectTitle: string
  description: string
  tables: DBTable[]
  relationships: Relationship[]
}

